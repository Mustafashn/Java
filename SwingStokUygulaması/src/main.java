    private void table_productMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_productMouseClicked
       
       int selected_row=table_product.getSelectedRow();
       txtfield_productname.setText(table_product.getValueAt(selected_row,0).toString());
       txtfield_price.setText(table_product.getValueAt(selected_row,2).toString());
       cmb_category.setSelectedItem(table_product.getValueAt(selected_row, 1).toString());
    }//GEN-LAST:event_table_productMouseClicked

    private void btn_deleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteProductActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_product.getModel();
        lbl_message.setText("");
        int selected_row=table_product.getSelectedRow();
        if (selected_row ==-1) {
            lbl_message.setText("Please select a row.");
        }
        else {
            model.removeRow(selected_row);
            lbl_message.setText("Product deleted.");
        }
    }//GEN-LAST:event_btn_deleteProductActionPerformed

