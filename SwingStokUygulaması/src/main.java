    private void btn_addProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addProductActionPerformed
        lbl_message.setText("");
        DefaultTableModel model = (DefaultTableModel)table_product.getModel();
        
        if(txtfield_productname.getText().trim().equals("")) {
            lbl_message.setText("Product name cannot be empty. ");
        }
        else {
            Object [] addObje = {txtfield_productname.getText(),cmb_category.getSelectedItem().toString(),txtfield_price.getText()};
            model.addRow(addObje);
        }
    }//GEN-LAST:event_btn_addProductActionPerformed

    private void btn_updateProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateProductActionPerformed
        lbl_message.setText("");
        DefaultTableModel model = (DefaultTableModel)table_product.getModel();
        int selected_row =table_product.getSelectedRow();
        
        if(selected_row==-1) {
            if(table_product.getRowCount()==0) {
                lbl_message.setText("Product table is empty.");
            }
            else {
                lbl_message.setText("Please select row for update.");
            }
        }
        else {
            model.setValueAt(txtfield_price.getText(), selected_row, 2);
            model.setValueAt(txtfield_productname.getText(),selected_row , 0);
            model.setValueAt(cmb_category.getSelectedItem(), selected_row, 1);
            lbl_message.setText("Product updated...");
        }
    }//GEN-LAST:event_btn_updateProductActionPerformed

